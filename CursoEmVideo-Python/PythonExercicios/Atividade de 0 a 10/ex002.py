# Desafio 002 - Crie um script Python que leia o dia, 
# mês e o ano de nascimento de uma pessoa 
# e mostre uma mensagem com a data formatada.

dia = int(input('Digite o dia de seu nascimento: '))
mes = int(input('Digite o mês de seu nascimento: '))
ano = int(input('Digite o ano de seu nascimento: '))

print(f'A data formatada de seu nascimento é: {dia}/{mes}/{ano}')

